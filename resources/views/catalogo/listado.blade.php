    <script>
        document.addEventListener('DOMContentLoaded', function () {
            var paginationLinks = document.querySelectorAll('.pagination .page-link');
            var prevButton = document.querySelector('.pagination .page-prev');
            var nextButton = document.querySelector('.pagination .page-next');

            paginationLinks.forEach(function (link, index) {
                link.addEventListener('click', function (event) {
                    event.preventDefault();
                    var pageNumber = index + 0;
                    if (pageNumber >= 1 && pageNumber <= 3) {
                        showPage(pageNumber);
                    }
                });
            });

            prevButton.addEventListener('click', function (event) {
                event.preventDefault();
                var currentPage = getCurrentPage();
                var prevPage = currentPage - 1;
                if (prevPage >= 1) {
                    showPage(prevPage);
                }
            });

            nextButton.addEventListener('click', function (event) {
                event.preventDefault();
                var currentPage = getCurrentPage();
                var nextPage = currentPage + 1;
                if (nextPage <= 3) {
                    showPage(nextPage);
                }
            });

            function showPage(pageNumber) {
                var allSections = document.querySelectorAll('.products');
                allSections.forEach(function (section, index) {
                    if (index + 1 === pageNumber) {
                        section.style.display = 'block';
                    } else {
                        section.style.display = 'none';
                    }
                });
            }

            function getCurrentPage() {
                var visibleSection = document.querySelector('.products[style="display: block;"]');
                var currentPage = Array.from(document.querySelectorAll('.products')).indexOf(visibleSection) + 1;
                return currentPage;
            }
        });
